#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		end;

	end = ft_strlen(s);
	i = 0;
	str = (char *)malloc(sizeof(char) * end + 1);
	if (!(str))
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
